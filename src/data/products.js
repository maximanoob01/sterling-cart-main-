export const getProductById = (id) => products.find(p => p.id === Number(id) || p.slug === id);
export const getProductBySlug = (slug) => products.find(p => p.slug === slug);
export const getProductsByCategory = (category) => products.filter(p => p.category === category);
export const getBestsellers = () => products.filter(p => p.badge === 'Bestseller');
export const getNewArrivals = () => products.filter(p => p.isNew || p.badge === 'New');
export const getRelatedProducts = (product, limit = 8) => {
  return products
    .filter(p => p.id !== product.id && (p.category === product.category || p.style === product.style))
    .slice(0, limit);
};
